package com.hsmt.entity;

//这个是页面到后台的映射类
public class PMenu {
	private int id;
	private String text;
	private String state;
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

}
